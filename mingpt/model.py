    # Model architecture
    n_layer = 8
    n_head = 8
    n_embd = 512
    # Data
    vocab_size = 29  # Will be overridden
    block_size = 512
    # Training
    embd_pdrop = 0.0
    resid_pdrop = 0.0
    attn_pdrop = 0.0
    # For wandb logging
